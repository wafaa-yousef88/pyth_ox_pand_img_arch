def codeToLang(code):
    code = code.lower()
    if len(code) == 2:
        for l in _iso639_languages:
            if l[2] == code:
                return l[0]
    elif len(code) == 3:
        for l in _iso639_languages:
            if l[3] == code:
                return l[0]
    return None

def langTo3Code(lang):
    lang = langEnglishName(lang)
    if lang:
        lang=lang.lower()
    for l in _iso639_languages:
        if l[0].lower() == lang:
            return l[3]
    return None

def langTo2Code(lang):
    lang = langEnglishName(lang)
    if lang:
        lang=lang.lower()
    for l in _iso639_languages:
        if l[0].lower() == lang:
            return l[2]
    return None

def langCode2To3(code):
    langTo3Code(codeToLang(code))

def langCode3To2(code):
    langTo2Code(codeToLang(code))

def langEnglishName(lang):
    lang = lang.lower()
    for l in _iso639_languages:
        if l[1].lower() == lang:
            return l[0]
    return None

def languages2Letter():
    languages = []
    for l in _iso639_languages:
        if l[2]:
            languages.append(l[2])
    return languages

